INSERT INTO BS.NAVIGATION
            (COORDINATS,ERROR_INDICATION,ID_SIGNAL)

WITH COMMUNICATIONS AS 
     (SELECT COORDINATS,ERROR_INDICATION, ID_SIGNAL FROM BS.COMMUNICATIONS_SECTOR_SP), 

     EARTH_S AS 
     (SELECT COORDINATES ,ID_SIGNAL FROM BS.EARTH_STATION )

SELECT SP.COORDINATS AS COORDINATS,
       ES.ID_SIGNAL AS ID_SIGNAL,
       SP.ERROR_INDICATION AS ERROR_INDICATION
FROM COMMUNICATIONS SP
LEFT JOIN EARTH_S ES ON SP.ID_SIGNAL = ES.ID_SIGNAL;